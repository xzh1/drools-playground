package impl.models;

public class Message {
	private String type;
    private String msgtext;
 
    public String getType() {
        return type;
    }
 
    public void setType(String type) {
        this.type = type;
    }
 
    public String getMsgtext() {
        return msgtext;
    }
 
    public void setMsgtext(String msgtext) {
        this.msgtext = msgtext;
    }
}
